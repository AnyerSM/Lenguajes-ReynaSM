package herencia;

/**
 *
 * @author T-107
 */
public abstract class Animal { 
//abstract solo se hereda y se permite tener metodos abstractos (no tiene cuerpo y al heredarse se le da cuerpo) solo debe tener hijos
 private String nombre;
 private int edad;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
 

}
